// ── Universal Log Schema ──────────────────────────────────────────────────────
// Every log source normalises into this structure before transmission.
// The Brain receives only this — never raw log formats.

type LogEvent struct {
	EventID   string `json:"event_id"`  // UUID generated by harvester
	Timestamp string `json:"timestamp"` // UTC ISO8601 — always UTC, always
	Platform  string `json:"platform"`  // windows | linux | network | file
	Source    string `json:"source"`    // hostname or device identifier

	Entity struct {
		Actor  string `json:"actor"`  // user / service / IP performing the action
		Target string `json:"target"` // host / service / endpoint being acted upon
	} `json:"entity"`

	Event struct {
		Type        string `json:"type"`         // auth | network | config | process | file | system
		Severity    int    `json:"severity"`     // 0–10
		RawEventID  int    `json:"raw_event_id"` // Windows Event ID or syslog facility
		Description string `json:"description"`  // normalised human-readable description
	} `json:"event"`

	MITRE struct {
		TechniqueID string `json:"technique_id"` // T1110, T1078, etc. — empty if unknown
		TacticName  string `json:"tactic_name"`  // credential-access, lateral-movement, etc.
	} `json:"mitre"`

	RawPayload string `json:"raw_payload"` // original log line — preserved for reference
}

// ── Config ────────────────────────────────────────────────────────────────────

type Config struct {
	// ZeroMQ
	ZMQAddress string // where to publish — brain subscribes here

	// WinRM targets
	WinRMHosts    []string
	WinRMUser     string
	WinRMPassword string
	WinRMPort     int

	// SSH / Linux targets
	SSHHosts    []string
	SSHUser     string
	SSHKeyPath  string
	SSHLogPaths []string // e.g. /var/log/auth.log, /var/log/syslog

	// Syslog UDP listener
	SyslogPort int

	// EVTX fallthrough — for systems where WinRM is disabled
	EVTXPaths []string

	// Polling intervals
	WinRMInterval time.Duration
	SSHInterval   time.Duration
	SyslogEnabled bool
}

